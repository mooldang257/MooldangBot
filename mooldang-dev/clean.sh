#!/bin/bash

# [청소부: 디스크 정화 스크립트]
# 이 스크립트는 도커 시스템에서 사용하지 않는 자원들을 강제로 정리하여 디스크 용량을 확보합니다.

echo "🧹 디스크 정화 작업을 시작합니다..."

# 1. 사용하지 않는 모든 자원 정리 (이미지, 컨테이너, 네트워크 포함)
# -a: 사용되지 않는 모든 이미지 삭제
# -f: 확인 절차 생략
echo "🚀 1/3: 사용하지 않는 도커 이미지 및 컨테이너 정리 중..."
docker system prune -a -f

# 2. 사용하지 않는 볼륨 정리
echo "📦 2/3: 사용하지 않는 도커 볼륨 정리 중..."
docker volume prune -f

# 3. 빌드 캐시 정리
echo "🔨 3/3: 도커 빌드 캐시 정리 중..."
docker builder prune -f

echo ""
echo "✅ 정화 작업이 완료되었습니다!"
echo "📊 현재 디스크 사용량:"
df -h | grep -E '^/dev/' || df -h
